 # ------------------ stack_stub_internal ------------------
 # we get an integer in at indicating which stub called us
 # ---------------------------------------------------------
	.ent	stack_stub_internal
stack_stub_internal:
.set noat
	br	$gp, dummy
dummy:	
	ldgp	$gp, 0($gp)	# fix gp so globals work
	lda	$26, snapshots_saved_ra_add
        s8addl  $at, $26, $26	# C pointers are 8 bytes in size
        ldq     $at, 0($26)	# we now have a pointer to the return address
        ldl     $26, 0($at)	# we have the return address which is 32 bits
        stl     $31, 0($at)	# set to zero to show we've consumed it
	ret	$31, ($26), 1
.set at
	.end	stack_stub_internal

 # ------------------ exn_stub_internal ------------------
 # $at contains which exn_stub called us
 # we use the return address to indicate which stub
 # remember not to touch $26 since that holds exnarg
 # ---------------------------------------------------------
	.globl  exn_stub_internal
	.ent	exn_stub_internal
exn_stub_internal:
.set noat
	br	$gp, exn_dummy
exn_dummy:	
	ldgp	$gp, 0($gp)

	ldl	$sp, 4($EXNPTR_REG)	# restore $sp of real handler

				# update global exnstub_maxsp ;  use Rpv as temp
				# save and restore $at
	stq	$at, -8($sp)
	lda	$27, exnstub_maxsp;
	ldl	$at, 0($27)
	cmplt	$at, $sp, $27
	cmovne  $27, $sp, $at
	lda	$27, exnstub_maxsp;
	stl	$at, 0($27)
	ldq	$at, -8($sp)

				# get code pointer and jump to real client handler
	lda	$27, exn_codeptr_table
        s4addl  $at, $27, $27		# value_t are 4 bytes in size
        ldl     $at, 0($27)		# we now have code we want to jump to
        stl     $31, 0($27)		# zero out to show exn consumed
	stl	$at, 0($EXNPTR_REG)	# restore code pointer in exn record
	mov     $at, $27		# set PV
	jmp	$31, ($27), 1
.set at
	.end	exn_stub_internal
	
	
		
